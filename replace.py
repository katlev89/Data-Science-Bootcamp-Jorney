#Saving a sentence as a single string:
original_sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."

#Printing the original sentence:
print(original_sentence)

#Replacing every "!" with an empty space
modified_sentence = original_sentence.replace("!", " ")

#Printing the modified sentence:
print(modified_sentence)

#Modifying the sentence to uppercase phrase:
uppercase_sentence = modified_sentence.upper()

#Printing the uppercase phrase:
print(uppercase_sentence)

#Reversing the sentence:
reversed_sentence = modified_sentence[::-1]

#Printing the reversed sentence:
print(reversed_sentence)