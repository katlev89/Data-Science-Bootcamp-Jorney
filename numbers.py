# Ask the user to enter three different integers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
num3 = int(input("Enter the third number: "))

# Calculate the sum of the numbers in the list
sum_of_numbers = num1 + num2 + num3

# Print the result
print("The sum of the three numbers is:", sum_of_numbers)

# Substraction of the second number from the first number:
print("The first number minus the second number is: ", num1 - num2)

# Multiplication of the third number by the first number:
print("The third number multiplied by the first number is: ", num3 * num1)

# Division of the sum of three numbers by the third number:
print("The sum devided by the third number is: ", sum_of_numbers / num3)