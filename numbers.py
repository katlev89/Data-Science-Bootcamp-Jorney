# Ask the user to enter three different integers
number_1 = int(input("Enter the first number: "))
number_2 = int(input("Enter the second number: "))
number_3 = int(input("Enter the third number: "))

# Calculate the sum of the numbers in the list
sum_of_numbers = number_1 + number_2 + number_3

# Print the result
print("The sum of the three numbers is:", sum_of_numbers)

# Substraction of the second number from the first number:
print("The first number minus the second number is: ", number_1 - number_2)

# Multiplication of the third number by the first number:
print("The third number multiplied by the first number is: ", number_3 * number_1)

# Division of the sum of three numbers by the third number:
print("The sum devided by the third number is: ", sum_of_numbers / number_3)