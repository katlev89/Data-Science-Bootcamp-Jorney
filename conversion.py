#Declare variables:
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

#Converting num1 to an integer:
num1_as_int = int(num1)

#Converting num2 to a float:
num2_as_float = float(num2)

#Converting num3 into a string:
num3_as_string = str(num3)

#Converting string1 to an integer:
string1_as_integer = int(string1)

#Printing out all variables on separate lines:
print("Original num1: ", num1)
print("Converted num1 to an integer: ", num1_as_int)

print("Original num2: ", num2)
print("Converted num2: ", num2_as_float)

print("Original num3: ", num3)
print("Converted num3 to a string: ", num3_as_string)

print("Original string1: ", string1)
print("Converted string1 to integer: ", string1_as_integer)